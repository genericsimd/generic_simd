int bitshuff(u_int8_t *firstdata,u_int8_t *getdata,int mode,u_int8_t mask,int bitlength)
{
    if(mode >=0)
    {
        bitshuff_lowbit_to_highbit(firstdata,getdata,mask,bitlength);
    }
    else bitshuff_highbit_to_lowbit(firstdata,getdata,mask,bitlength);
    return 0;
}


int bitshuff_lowbit_to_highbit(u_int8_t *firstdata,u_int8_t *getdata,u_int8_t mask,int bitlength)
{
    *getdata &= ~mask;
    *getdata |= (*firstdata & mask)<<(sizeof(u_int8_t)*PERBITINBYTE - bitlength);
    return 0;
}


int bitshuff_highbit_to_lowbit(u_int8_t *firstdata,u_int8_t *getdata,u_int8_t mask,int bitlength)
{
    *getdata &= mask;
    *getdata |= ((~mask) & *firstdata)>>bitlength;
    return 0;
}

int bitmove(u_int8_t *pdata1,u_int8_t *pdata2,int mode,int bitoffset,int ibytecount)
{
    
  // int mode = -1;
    int bitmaskstart =  sizeof(u_int8_t)*PERBITINBYTE - bitoffset;
    u_int8_t mask = getmode(bitmaskstart);
    if(bitoffset == 0)
    {
        memcpy(pdata2,pdata1,ibytecount);
    }
    else
    {
        bitshuff(pdata1,pdata2,mode,mask,bitoffset);                 //for first bytes 
        int i = 1;
        for(; i < ibytecount; i++)
        {   
           bitshuff(pdata1+i,pdata2 + i - 1,mode,mask,bitoffset);    //for low bits shuff
           mode = -mode;
           bitshuff(pdata1+i,pdata2 +i,mode,mask,bitoffset);          //for high bits shuff
           mode = -mode;
        }
    }
    return 0;
}

