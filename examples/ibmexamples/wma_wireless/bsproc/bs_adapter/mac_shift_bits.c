/*the pdata stored with bit in byte data, and validate data at LST(lowest bit),
 *we will combinate the data to pdesdata. the Byte data contain 8 bit data.
 */
int bits_to_byte(u_int8_t *pdata,int ibitlength,u_int8_t *pdestdata,int *ibitstartoffset,int * ibyteoffset)
{
    int iloop = 0;
    int ibytecal = 0,ibitoffset =0;
    if(ibyteoffset != NULL)
        ibytecal = *ibyteoffset;
    if(ibitstartoffset != NULL)
        ibitoffset= *ibitstartoffset;
    //int icount = *ibitstartoffset;
    while (ibitoffset != 0)
    {
        *(pdestdata + ibytecal) = (((*(pdata+iloop)) << (7 - ibitoffset))| ((*(pdestdata+ibytecal))&getcombinatemode(ibitoffset)));
        iloop++;
        ibitoffset++;
        ibytecal += ibitoffset/PERBITINBYTE;
        ibitoffset %= PERBITINBYTE;
    }
    pdata += iloop;
    while ((iloop + 8) <= ibitlength)
    {
        *(pdestdata + ibytecal) = (*pdata<<7) | (*(pdata+1) << 6) | (*(pdata+2) <<5) | (*(pdata+3) << 4) | (*(pdata+4)<< 3 ) |(*(pdata+5) << 2) | ( *(pdata + 6) << 1) | *(pdata+7);
        iloop += 8;
        pdata += 8;
        ibytecal ++;
    }
    while (iloop  < ibitlength)
    {
        *(pdestdata + ibytecal) = (((*pdata) << (7 - ibitoffset))| ((*(pdestdata+ibytecal))&getcombinatemode(ibitoffset)));
        iloop++;
        pdata++;
        ibitoffset++;
        ibytecal += ibitoffset/PERBITINBYTE;
        ibitoffset %= PERBITINBYTE;
    }
    if(ibyteoffset !=NULL)
        *ibyteoffset = ibytecal;
    if(ibitstartoffset != NULL)
        *ibitstartoffset = ibitoffset;
    return 0;
}
